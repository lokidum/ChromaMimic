const BRAND = "ChromaMimic";
const STUDIO = "Nunik Co.";

export function dl(blob: Blob, name: string) {
  const a = document.createElement("a");
  a.href = URL.createObjectURL(blob);
  a.download = name;
  a.click();
  setTimeout(() => URL.revokeObjectURL(a.href), 4000);
}

/* ---------- .cube (Adobe Cube LUT v1.0) ---------- */
export function lutToCube(
  lut: Float32Array,
  size: number,
  title: string,
  meta: { method: Method; space: Space },
): string {
  let s = `# Generated by ${BRAND} (${STUDIO})\n`;
  s += `# Method: ${meta.method} | Input space: ${
    meta.space === "log" ? "see note below" : "sRGB / Rec.709"
  } | Built: ${new Date().toISOString().slice(0, 10)}\n`;
  if (meta.space === "log")
    s += `# NOTE: frames were tagged as log. Apply a Color Space Transform (log -> Rec.709) BEFORE this LUT, or results will look wrong.\n`;
  s += `TITLE "${title}"\nLUT_3D_SIZE ${size}\nDOMAIN_MIN 0.0 0.0 0.0\nDOMAIN_MAX 1.0 1.0 1.0\n`;
  const lines: string[] = [];
  for (let i = 0; i < lut.length; i += 3) {
    lines.push(`${lut[i].toFixed(6)} ${lut[i + 1].toFixed(6)} ${lut[i + 2].toFixed(6)}`);
  }
  return s + lines.join("\n") + "\n";
}

/* ---------- .xmp (Lightroom / ACR preset, grade only) ---------- */
export function exportXMP(g: Grade) {
  const v = (x: number, m: number) => Math.round(x * m);
  const xmp = `<x:xmpmeta xmlns:x="adobe:ns:meta/">
 <rdf:RDF xmlns:rdf="http://www.w3.org/1999/02/22-rdf-syntax-ns#">
  <rdf:Description rdf:about="" xmlns:crs="http://ns.adobe.com/camera-raw-settings/1.0/"
   crs:Version="15.0" crs:ProcessVersion="11.0"
   crs:Temperature="${v(g.temp, 2000) >= 0 ? "+" : ""}${v(g.temp, 2000)}"
   crs:Tint="${v(g.tint, 50)}"
   crs:Exposure2012="${(g.exp * 1.5).toFixed(2)}"
   crs:Contrast2012="${v(g.con, 100)}"
   crs:Highlights2012="${v(g.hig, 100)}"
   crs:Shadows2012="${v(g.sha, 100)}"
   crs:Saturation="${v(g.sat, 100)}"
   crs:Vibrance="${v(g.vib, 100)}"
   crs:Blacks2012="${v(g.fad, 40)}"
   crs:HasSettings="True">
   <crs:Name><rdf:Alt><rdf:li xml:lang="x-default">${BRAND} Grade</rdf:li></rdf:Alt></crs:Name>
  </rdf:Description>
 </rdf:RDF>
</x:xmpmeta>`;
  dl(new Blob([xmp], { type: "application/rdf+xml" }), `chromamimic_grade_${Date.now()}.xmp`);
}

